package com.example.studentnoob;

public class StudentSub {
    public String name,mssv;

    public StudentSub(String name, String mssv) {
        this.name = name;
        this.mssv = mssv;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMssv() {
        return mssv;
    }

    public void setMssv(String mssv) {
        this.mssv = mssv;
    }
}
